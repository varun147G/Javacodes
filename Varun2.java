class Varun2 {
    public static void main(String[] args) {
        int a = 15;
        int b = 20;
        int sum = a + b;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(sum - a);
        System.out.println(sum - b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(b % a);
    }
}