class Unary3 {
    public static void main(String[] args) {
        int a = 3;
        int b = a++;
        int x = 1;
        int y = ++x;
        int p = 3;
        int q = p--;
        int c = 5;
        int d = --c;
        System.out.println("a value is " + a + " b value is " + b + " and x value is " + x + " y value is " + y
                + " and p value is " + p + " q value is " + q + " and c value is " + c + " d value is " + d);

    }
}