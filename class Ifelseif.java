class Ifelseif {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        if (a >= b) {
            System.out.println(a + " is greaterthan " + b);
        } else if (a == b) {
            System.out.println(a + " is equal to " + b);
        } else {
            System.out.println(a + " is lesser than " + b);
        }
    }
}
